package com.ko.lct.job.geocoding.businessobjects;

public class Product {
    
    private String key;
    private String description;
    private String beverageCategory;
    private String brand;
    private String flavor;
    private String flavorKey;
    private String container;
    private String baseSize;
    private String packageSize;
    private String universalProductCode;
    
    public String getKey() {
        return key;
    }
    public void setKey(String key) {
        this.key = key;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public String getBeverageCategory() {
        return beverageCategory;
    }
    public void setBeverageCategory(String beverageCategory) {
        this.beverageCategory = beverageCategory;
    }
    public String getBrand() {
        return brand;
    }
    public void setBrand(String brand) {
        this.brand = brand;
    }
    public String getFlavor() {
        return flavor;
    }
    public void setFlavor(String flavor) {
        this.flavor = flavor;
    }
    public String getFlavorKey() {
        return flavorKey;
    }
    public void setFlavorKey(String flavorKey) {
        this.flavorKey = flavorKey;
    }
    public String getContainer() {
        return container;
    }
    public void setContainer(String container) {
        this.container = container;
    }
    public String getBaseSize() {
        return baseSize;
    }
    public void setBaseSize(String baseSize) {
        this.baseSize = baseSize;
    }
    public String getPackageSize() {
        return packageSize;
    }
    public void setPackageSize(String packageSize) {
        this.packageSize = packageSize;
    }
    public String getUniversalProductCode() {
        return universalProductCode;
    }
    public void setUniversalProductCode(String universalProductCode) {
        this.universalProductCode = universalProductCode;
    }
}
